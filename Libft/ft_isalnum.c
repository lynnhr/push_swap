#include "libft.h"

int	ft_isalnum(int c)
{
	int	isalpha;

	isalpha = ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
	return (isalpha || (c >= '0' && c <= '9'));
}
